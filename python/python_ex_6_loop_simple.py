# 사용자로부터 구구단으로 출력할 단(숫자)을 입력받습니다.
try:
    dan = int(input("출력할 구구단의 단(2~9)을 입력하세요: "))

    # 입력된 숫자의 유효성 검사
    if 2 <= dan <= 9:
        print(f"--- {dan}단 ---")
        # for 반복문: 1부터 9까지 순회합니다.
        for i in range(1, 10):
            # i는 매 반복마다 1, 2, 3, ..., 9로 변합니다.
            print(f"{dan} x {i} = {dan * i}")
    else:
        print("오류: 2에서 9 사이의 숫자만 입력해주세요.")

except ValueError:
    # 정수가 아닌 값을 입력했을 때의 예외 처리
    print("오류: 숫자로만 입력해야 합니다.")